pub type CharPixel = [Vec<u8>; 7];

pub fn get_char_pixel(r#char: char) -> CharPixel {
    return match r#char {
        'A'..='Z' => get_big_char(char),
        'a'..='z' => get_smal_char(char),
        '0'..='9' => get_number(char),
        _ => get_char(char),
    };
}

